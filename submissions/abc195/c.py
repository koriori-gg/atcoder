n=int(input())
if n<=999:
  print(0)
elif n>=1000 and n<1000000:
  print(n-999)
elif n>=1000000 and n<1000000000:
  print(n-999+n-999999)
elif n>=1000000000 and n<1000000000000:
  print(n-999+n-999999+n-999999999)
elif n>=1000000000000 and n<1000000000000000:
  print(n-999+n-999999+n-999999999+n-999999999999)
elif n>=1000000000000000 and n<1000000000000000000:
  print(n-999+n-999999+n-999999999+n-999999999999+n-999999999999999)
